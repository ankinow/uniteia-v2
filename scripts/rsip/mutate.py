def write_mutation_report(results: list[tuple[str, bool]], reflection: dict) -> Path:
    report = {
        "ts": datetime.now(timezone.utc).isoformat(),
        "schema": "p4-mutation.v1",
        "reflection_ref": reflection.get("ts", "unknown"),
        "mutations": [{"target": r[0], "changed": r[1]} for r in results],
        "changed_count": sum(1 for _, c in results if c),
    }
    L4_DIR.mkdir(parents=True, exist_ok=True)
    path = L4_DIR / "mutate-latest.json"
    path.write_text(json.dumps(report, indent=2, ensure_ascii=False))
    return path


def main():
    import argparse
    parser = argparse.ArgumentParser(description="P4 RSIP — MUTATE")
    parser.add_argument("--write", action="store_true", help="Actually apply diffs")
    parser.add_argument("--reflect", type=str, default=None,
                        help="Path to reflection JSON")
    args = parser.parse_args()

    print(f"╔══ P4 RSIP: MUTATE ══╗")
    print(f"║ mode={'write' if args.write else 'dry-run'}")

    reflection = load_reflection()

    results = []

    # AGENTS.md
    msg, changed = mutate_agents(reflection)
    results.append((msg, changed))
    print(f"  {'✏️' if changed else '–'} {msg}")

    # daily_ops.py
    msg, changed = scaffold_daily_ops()
    results.append((msg, changed))
    print(f"  {'✏️' if changed else '–'} {msg}")

    # build.py
    msg, changed = scaffold_build()
    results.append((msg, changed))
    print(f"  {'✏️' if changed else '–'} {msg}")

    # metrics.py
    msg, changed = scaffold_metrics()
    results.append((msg, changed))
    print(f"  {'✏️' if changed else '–'} {msg}")

    report_path = write_mutation_report(results, reflection)
    print(f"  → {report_path}")

    if not args.write:
        print(f"\n[DRY RUN] No files changed. Run with --write to apply.")
    else:
        print(f"\n✅ Mutations applied ({sum(1 for _, c in results if c)} changed)")

    return 0


if __name__ == "__main__":
    sys.exit(main())
