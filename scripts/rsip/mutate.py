def load_reflection() -> dict:
    path = L4_DIR / "reflect-latest.json"
    if path.exists():
        return json.loads(path.read_text())
    return {"branches": [], "drift": {"status": "first_run"}}


def mutate_agents(reflection: dict) -> tuple[str, bool]:
    """Append P4 RSIP section to AGENTS.md. Returns (diff, changed)."""
    path = MUTATION_TARGETS["AGENTS.md"]["path"]
    if not path.exists():
        return "AGENTS.md not found", False

    content = path.read_text()

    # Check if P4 section already exists
    if "## P4 RSIP∞ Eval Loop" in content:
        return "P4 section exists (no change)", False

    p4_section = f"""
## P4 RSIP∞ Eval Loop (auto-generated {datetime.now().strftime('%Y-%m-%d')})

### Pipeline
```
observe → reflect → mutate → consolidate → audit → Ω(merge|reject|rollback)
```

### Triggers
- Daily 08:00 UTC: p4-audit-trail.py --auto (health check)
- Weekly: rsip-cycle.sh (full observe→reflect→mutate→consolidate→audit)
- On cargo: p4-audit-trail.py (explicit invocation)

### P4 Agents (RSIP sub-roles)

| Agent | Role | Tool Access | Stop Condition |
|-------|------|-------------|----------------|
| `observe` | Ingest L2 + metrics | read: memory/L2, meta/archive | data ingested |
| `reflect` | MA-ToT k=3 reflection | read: obs-latest, L2 | drift quantified |
| `mutate` | Generate diffs | read: AGENTS, scripts · write: diff | diff reviewed |
| `consolidate` | L2→L3 promotion | read: L2 · write: L3, wiki | gate passed |
| `audit` | External validation | read: P1.3 audit, NIM API | safety verified |

### Boundaries
- Forbidden: repo deletion, git rewrite, API key changes, DNS, eval threshold <90%
- Dry-run first: simulate mutation via git diff before commit
- Zero-trust MCP: verify all tool outputs
- human@write: destructive fs/git ops pause for approval

### Version
- schema: p4-rsip.v1
- generated: {datetime.now(timezone.utc).isoformat()}
- source: scripts/rsip/mutate.py
"""

    path.write_text(content + p4_section)
    return f"P4 section appended to AGENTS.md (+{len(p4_section)} chars)", True


